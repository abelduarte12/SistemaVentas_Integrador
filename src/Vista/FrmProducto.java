    private void tablaProductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaProductoMouseClicked
        
        int fila = tablaProducto.rowAtPoint(evt.getPoint());
        txtId.setText(tablaProducto.getValueAt(fila, 0).toString());
        txtCodigo.setText(tablaProducto.getValueAt(fila, 1).toString());
        txtNombre.setText(tablaProducto.getValueAt(fila, 2).toString());
        cboProveedor.setSelectedItem(tablaProducto.getValueAt(fila, 3).toString());
        txtCantidad.setText(tablaProducto.getValueAt(fila, 4).toString());
        txtPrecio.setText(tablaProducto.getValueAt(fila, 5).toString());
        
        
    }//GEN-LAST:event_tablaProductoMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
          if (!"".equals(txtId.getText())) {
            int pregunta = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar");
            if (pregunta == 0) {
                int id = Integer.parseInt(txtId.getText());
                proDao.EliminarProductos(id);
                LimpiarTabla();
                LimpiarProductos();
                ListarProductos();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecciona una fila");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if ("".equals(txtId.getText())) {
            JOptionPane.showMessageDialog(null, "Seleecione una fila");
        } else {
            if (!"".equals(txtCodigo.getText()) || !"".equals(txtNombre.getText()) || !"".equals(txtCantidad.getText()) 
                    || !"".equals(txtPrecio.getText())) {
                pro.setCodigo(txtCodigo.getText());
                pro.setNombre(txtNombre.getText());
                pro.setProveedor(cboProveedor.getSelectedItem().toString());
                pro.setStock(Integer.parseInt(txtCantidad.getText()));
                pro.setPrecio(Double.parseDouble(txtPrecio.getText()));
                pro.setId(Integer.parseInt(txtId.getText()));
                proDao.ModificarProductos(pro);
                JOptionPane.showMessageDialog(null, "Producto Modificado");
                LimpiarTabla();
                ListarProductos();
                LimpiarProductos();
            }
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

