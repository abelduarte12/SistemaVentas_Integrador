    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        if (!"".equals(txtCodigo.getText()) || !"".equals(txtNombre.getText()) || 
                !"".equals(cboProveedor.getSelectedItem()) || !"".equals(txtCantidad.getText()) || 
                !"".equals(txtPrecio.getText())) {
            pro.setCodigo(txtCodigo.getText());
            pro.setNombre(txtNombre.getText());
            pro.setProveedor(cboProveedor.getSelectedItem().toString());
            pro.setStock(Integer.parseInt(txtCantidad.getText()));
            pro.setPrecio(Double.parseDouble(txtPrecio.getText()));
            proDao.RegistrarProductos(pro);
            JOptionPane.showMessageDialog(null, "Productos Registrado");
            LimpiarTabla();
            ListarProductos();
            LimpiarProductos();
        } else {
            JOptionPane.showMessageDialog(null, "Los campos estan vacios");
        }
    
    }//GEN-LAST:event_btnGrabarActionPerformed

