    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        
          if (!"".equals(txtRuc.getText()) || !"".equals(txtNombre.getText()) || 
                  !"".equals(txtTelefono.getText()) || !"".equals(txtDireccion.getText())|| !"".equals(txtRazon.getText())) {
            pr.setRuc(Integer.parseInt(txtRuc.getText()));
            pr.setNombre(txtNombre.getText());
            pr.setTelefono(Integer.parseInt(txtTelefono.getText()));
            pr.setDireccion(txtDireccion.getText());
            pr.setRazon(txtRazon.getText());
            prDao.RegistrarProveedor(pr);
            JOptionPane.showMessageDialog(null, "Proveedor Registrado");
            LimpiarTabla();
            ListarProveedor();
        } else {
            JOptionPane.showMessageDialog(null, "Los campos esta vacios");
        }
        
        
        
    }//GEN-LAST:event_btnGrabarActionPerformed

