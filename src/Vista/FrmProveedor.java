    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
         if ("".equals(txtID.getText())) {
            JOptionPane.showMessageDialog(null, "Seleecione una fila");
        } else {
            if (!"".equals(txtRuc.getText()) || !"".equals(txtNombre.getText()) || 
                    !"".equals(txtTelefono.getText()) || !"".equals(txtDireccion.getText())
                    || !"".equals(txtRazon.getText())) {
                pr.setRuc(Integer.parseInt(txtRuc.getText()));
                pr.setNombre(txtNombre.getText());
                pr.setTelefono(Integer.parseInt(txtTelefono.getText()));
                pr.setDireccion(txtDireccion.getText());
                pr.setRazon(txtRazon.getText());
                pr.setId(Integer.parseInt(txtID.getText()));
                prDao.ModificarProveedor(pr);
                JOptionPane.showMessageDialog(null, "Proveedor Modificado");
                LimpiarTabla();
                ListarProveedor();
            }
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

